      </SfList>
    </SfFooterColumn>
    <SfFooterColumn title="Social" class="desktop-only">
      <div class="footer__socials">
        <a v-for="item in social" :href="item.url" target="_blank" >
          <SfImage
            class="footer__social-image"
            :key="item.id"
            :src="addBasePath('/icons/' + item.brand + '.svg')"
            :alt="item.brand"
            :width="32"
            :height="32"
          />
        </a>
      </div>
    </SfFooterColumn>

    <SfFooterColumn title="Language" class="desktop-only">
      <LocaleSelector class="py-3.5" />
    </SfFooterColumn>

    <SfFooterColumn class="desktop-only">
      <template>
        <ValidationObserver v-slot="{ handleSubmit }" key="log-in">
          <form
            @submit.prevent="handleSubmit(onSubscribe)"
            style="display: flex; height: 46px"
          >
            <ValidationProvider rules="required|email" v-slot="{ errors }">
              <SfInput
                class="sf-input--outline"
                type="text"
                :valid="!errors[0]"
                v-model="emailAddress"
                :errorMessage="errors[0]"
                placeholder="Type your email address"
                style="
                  width: 242px;
                  height: 46px;
                  font-size: 16px;
                  color: #43464e;
                  background-color: #f1f2f3;
                  --input-border-color: #f1f2f3;
                "
              />
            </ValidationProvider>
            <SfButton style="width: 116px" type="submit" :disabled="loading"
              >Subscribe</SfButton
            >
          </form>
        </ValidationObserver>
      </template>
    </SfFooterColumn>
    <div class="sf-footer-column smartphone-only">
      <div
        style="
          display: flex;
          flex-direction: column;
          jusify-content: space-around;
          align-items: center;
