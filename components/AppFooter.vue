<script>
import {
  SfFooter,
  SfList,
  SfImage,
  SfMenuItem,
  SfInput,
  SfButton,
  SfLink
} from '@storefront-ui/vue';
import { ref } from '@nuxtjs/composition-api';
import { addBasePath } from '@vue-storefront/core';
import { useNewsLetter } from '@vue-storefront/odoo';
import LocaleSelector from '~/components/LocaleSelector';
import { ValidationProvider, ValidationObserver } from 'vee-validate';
import { useUiNotification } from '~/composables';

export default {
  components: {
    ValidationProvider,
    ValidationObserver,
    SfLink,
    SfFooter,
    LocaleSelector,
    SfList,
    SfImage,
    SfMenuItem,
    SfInput,
    SfButton
  },
  setup() {
    const { sendSubscription, loading } = useNewsLetter();
    const emailAddress = ref('');
    const { send } = useUiNotification();

    const onSubscribe = async (emailAddress) => {
      const data = await sendSubscription({ email: emailAddress });

      if (data?.subscribed) {
        send({
          message: 'Subscribe successfull!',
          type: 'success'
        });
      }
      if (!data?.subscribed) {
        send({
          message: 'Something wrong!',
          type: 'danger'
        });
      }
    };

    return {
      onSubscribe,
      emailAddress,
      sendSubscription,
      loading,
      addBasePath
    };
  },
  data() {
    return {
      aboutUs: ['Who we are', 'Quality in the details', 'Customer Reviews'],
      departments: ['Women fashion', 'Men fashion', 'Kidswear', 'Home'],
      help: ['Customer service', 'Size guide', 'Contact us'],
      paymentsDelivery: ['Purchase terms', 'Guarantee'],
      social: [
        {
          id: 1,
          brand: 'facebook',
          url: 'https://www.facebook.com/'
        },
        {
          id: 2,
          brand: 'pinterest',
          url: 'https://www.pinterest.com/'
        },
        {
          id: 3,
          brand: 'google',
          url: '/'
        },
        {
          id: 4,
          brand: 'twitter',
          url: 'https://twitter.com/'
        },
        {
          id: 4,
          brand: 'youtube',
          url: 'https://www.youtube.com/'
        }
      ],
      isMobile: false,
      desktopMin: 1024
    };
  }
};
</script>

